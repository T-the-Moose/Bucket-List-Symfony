    public function findAllPublished(): Paginator {
        $query = $this->createQueryBuilder('w')
            ->leftJoin('w.categories', 'c')
            ->addSelect('c')
            ->andWhere('w.isPublished = true')
            ->addOrderBy('w.dateCreated', 'DESC')
            ->setMaxResults(10)
            ->getQuery();

        $paginator = new Paginator($query);
        return $paginator;
    }
